package for_circulation;
// 输出数字1-5
// 输出数字5-1
public class test1_1 {
    public static void main(String[] args) {
        for(int i = 1;i <= 5;i++){
            System.out.println(i);
        }
        System.out.println("---------------");
        for(int n = 5; n >= 1;n--){
            System.out.println(n);
        }
    }
}
